package com.unionblue.wechat.model;

import java.io.Serializable;

/**
 * Created by 18501 on 2018/10/29.
 */
public class OrderReturnJosn implements Serializable {

    //订单id
    private String id;

    //订单编号
    private String fbillno;

    //国家
    private String country;

    //订单金额
    private String TotalPaidPrice;

    //可退税金
    private String TotalInvoiceTaxPrice;

    //币种
    private String countryCurrency;

    //发票数量
    private String InvoiceQuantity;

    //订单状态
    private String status;

    //订单创建时间
    private String createtime;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getFbillno() {
        return fbillno;
    }

    public void setFbillno(String fbillno) {
        this.fbillno = fbillno;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getTotalPaidPrice() {
        return TotalPaidPrice;
    }

    public void setTotalPaidPrice(String totalPaidPrice) {
        TotalPaidPrice = totalPaidPrice;
    }

    public String getTotalInvoiceTaxPrice() {
        return TotalInvoiceTaxPrice;
    }

    public void setTotalInvoiceTaxPrice(String totalInvoiceTaxPrice) {
        TotalInvoiceTaxPrice = totalInvoiceTaxPrice;
    }

    public String getCountryCurrency() {
        return countryCurrency;
    }

    public void setCountryCurrency(String countryCurrency) {
        this.countryCurrency = countryCurrency;
    }

    public String getInvoiceQuantity() {
        return InvoiceQuantity;
    }

    public void setInvoiceQuantity(String invoiceQuantity) {
        InvoiceQuantity = invoiceQuantity;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getCreatetime() {
        return createtime;
    }

    public void setCreatetime(String createtime) {
        this.createtime = createtime.replaceAll("T", " ");
    }
}
