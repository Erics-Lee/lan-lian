package com.unionblue.wechat.model;

import java.io.Serializable;
import java.util.List;

/**
 * Created by 18501 on 2018/11/23.
 */
public class InvoiceNumber implements Serializable{

    //发票状态
    private String status;

    //国家
    private String Country;

    //发票数量
    private String Length;

    //币别符号
    private String currencysymbol;

    //发票税金
    private String AllInvoiceTaxPrice;

    //年度起退线
    private String YearLimitAmt;

    //发票列表
    private List<Invoices> Invoices;

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getCountry() {
        return Country;
    }

    public void setCountry(String country) {
        Country = country;
    }

    public String getLength() {
        return Length;
    }

    public void setLength(String length) {
        Length = length;
    }

    public String getCurrencysymbol() {
        return currencysymbol;
    }

    public void setCurrencysymbol(String currencysymbol) {
        this.currencysymbol = currencysymbol;
    }

    public String getAllInvoiceTaxPrice() {
        return AllInvoiceTaxPrice;
    }

    public void setAllInvoiceTaxPrice(String allInvoiceTaxPrice) {
        AllInvoiceTaxPrice = allInvoiceTaxPrice;
    }

    public String getYearLimitAmt() {
        return YearLimitAmt;
    }

    public void setYearLimitAmt(String yearLimitAmt) {
        YearLimitAmt = yearLimitAmt;
    }

    public List<com.unionblue.wechat.model.Invoices> getInvoices() {
        return Invoices;
    }

    public void setInvoices(List<com.unionblue.wechat.model.Invoices> invoices) {
        Invoices = invoices;
    }
}
