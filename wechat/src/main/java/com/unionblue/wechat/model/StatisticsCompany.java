public class StatisticsCompany {

    private String CompanyNo;
    private String RawCompanyName;
    private String status;
    private String AllInvoices;
    private String AllowInvoices;

    public String getCompanyNo() {
        return CompanyNo;
    }

    public void setCompanyNo(String companyNo) {
        CompanyNo = companyNo;
    }

    public String getRawCompanyName() {
        return RawCompanyName;
    }

    public void setRawCompanyName(String rawCompanyName) {
        RawCompanyName = rawCompanyName;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getAllInvoices() {
        return AllInvoices;
    }

    public void setAllInvoices(String allInvoices) {
        AllInvoices = allInvoices;
    }

    public String getAllowInvoices() {
        return AllowInvoices;
    }

    public void setAllowInvoices(String allowInvoices) {
        AllowInvoices = allowInvoices;
    }
}
